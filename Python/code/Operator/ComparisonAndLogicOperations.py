# Comparison Operations
# -> ==
print (1==1) #True

# -> !=
print(2!=2) #False

# -> >
print(2>1) #True

# -> <
print(2<1) #False

# -> >=
print(3>=2) #True

# -> <=
print(3<=2) #False

# Logic Operations
# -> AND
print(2>1 and 3>2) #True

# OR
print(2>1 or 3>2) #True

# NOT
print(not 2>3) #True



