# Operator Aritmatika
a = 1
b = 2

# +
print(a+b) #3

# - 
print(a-b) #-1

# *
print(a*b) # 2

# /
print(a/b) # 0.5

# %
print(a%b) #2

# **
print(a**b) #1

# //
print(a//b) #0 -> floor division, pembagian dibulatkan ke bawah

# Operator Penugasan
# -> Operator ==
a = 2
print(a) #2

#-> Operator Aritmatika + Operator Penugasan
a += 3
print(a) #5 -> berlaku juga dengan operator aritmatika yang lain


